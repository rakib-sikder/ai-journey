name = "rakib"

print(len(name)) # length of string
print(name.find("a")) # find the index of a character in string
print(name.count("r")) # count the number of times a character appears in string
print(name.upper()) # convert string to upper case
print(name.lower()) # convert string to lower case
print(name.replace("a", "A")) # replace a character in string
print(name.isalpha()) # check if string is alphabetic
print(name.isdigit()) # check if string is digit
print(name.capitalize()) # capitalize the first letter of string
print(name *10) # repeat the string 10 times